# 1- Eu ligaria o primeiro interruptor e sairia a primeira sala para verificar a lâmpada, nisso caimos em algumas situações

#   1.1. - Digamos que ao chegar no primeiro cômodo a luz esteja acesa, nisso eu poderia voltar a sala e ligar o próximo interruptor 
#          e verificar se a próxima sala acende a luz, caso isso aconteça, a teoria que as lâmpadas estão ligadas em sequência se confirma,
#          e teremos confirmado a posição das 3 com apenas duas tentativas, mesmo se eu errar na segunda tentativa, eu já saberei a localização das outras duas.

#   1.2. - Se eu errar de primeira, e ela estiver na segunda, a teoria das lâmpadas em sequência vai para o ralo, mas ainda terei
#          mais uma tentativa para acertar a próxima lâmpada, errando ou acertando eu já sei a localização das outras duas.

# 2- Caso eu erre nas duas tentativas a localização da primeira lâmpada estará no último cômodo, aqui não temos como confirmar em duas tentativas, mas caímos em mais 2 situações:

#   2.1. - Se eu errei os dois primeiros cômodos, significa que a lâmpada acesa está na 3 sala, o que se a teoria das lâmpadas em sequência estiver certa, a ordem está invertida,
#          O que significa que eu já sei a localização das outras duas, porém iria precisar de uma terceira tentativa para confirmar.

#   2.2. - Se a teoria estiver errada e a ligação estiver no modo aleatório, o interruptor do meio poderia ligar qualquer uma das salas restantes.

#   Em resumo, no pior cenário eu errando as 2, eu precisaria de uma terceira tentativa para poder confirmar qual interruptor liga qual.

